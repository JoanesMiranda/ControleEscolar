package br.com.controleescolar.model;

/**
 *
 * @author Joanes
 */
public class Aluno {
    
    private int idaluno;
    private String nome;
    private String matricula;
    private String codcartaoarduino;

    /**
     * @return the idaluno
     */
    public int getIdaluno() {
        return idaluno;
    }

    /**
     * @param idaluno the idaluno to set
     */
    public void setIdaluno(int idaluno) {
        this.idaluno = idaluno;
    }

    /**
     * @return the nome
     */
    public String getNome() {
        return nome;
    }

    /**
     * @param nome the nome to set
     */
    public void setNome(String nome) {
        this.nome = nome;
    }

    /**
     * @return the matricula
     */
    public String getMatricula() {
        return matricula;
    }

    /**
     * @param matricula the matricula to set
     */
    public void setMatricula(String matricula) {
        this.matricula = matricula;
    }

    /**
     * @return the codcartaoarduino
     */
    public String getCodcartaoarduino() {
        return codcartaoarduino;
    }

    /**
     * @param codcartaoarduino the codcartaoarduino to set
     */
    public void setCodcartaoarduino(String codcartaoarduino) {
        this.codcartaoarduino = codcartaoarduino;
    }
    
}
